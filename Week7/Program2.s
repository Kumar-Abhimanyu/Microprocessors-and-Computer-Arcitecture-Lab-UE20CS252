.TEXT
	MOV R0,#0 ; R0=X
	MOV R1,#14 ;R1=Y
	MOV R7,#0
	LDR R8,=NUM
	LDR R8,[R8]
	LDR R2,=STR
 LOOP:	SWI 0X204 ; DISPLAY STRING ON SCREEN(R2)
	BL SUM
	CMP R0,#70
	ADDNE R0,R0,#1
	SWIEQ 0X011
	B LOOP
 SUM:	CMP R7,R8
	ADDNE R7,R7,#1
	BNE SUM
	SWI 0X206 ; CLEAR THE SCREEN
	MOV R7,#0
	MOV PC,LR

.DATA
STR:.ASCIZ "HELLO WORLD"
NUM:.WORD 30000